class DataTypeDemo // class been created
{
   public static void main(String args[]) // calling a main method 
   {  
   int value=50;  // taking int value
   float marks=90.5f; // taking float value with ending f 
   double percentage= 98.785d; // taking double value ending with double
   char grade='A'; // taking a character is a
   short number =(short) -32769; // typecasting of short
   long hugeNumber= 4585964545487l; // taking a long or huge number with  using long
   boolean status=true; // taking a boolean as true
   byte b=(byte)128; // typecasting of byte
    // printing all the line by line
   System.out.println("the value of number is" +value);
   System.out.println("the value of marks is" +marks);
   System.out.println("the value of percentage is" +percentage);
   System.out.println("the grade  is" +grade);
   System.out.println("the value  is" +number);
   System.out.println("the huge Number is" +hugeNumber);
   System.out.println("the status is" +status);
   System.out.println("the value of b is" +b);
   } // end of main method
   } // end of main class